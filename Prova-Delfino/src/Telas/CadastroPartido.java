    private void btnInserirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInserirMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbPartidos.getModel();
        PartidoRepositorio partidoRepositorio = new PartidoRepositorio();
        Partido partido = new Partido();
        String nome = null;
        
            nome = JOptionPane.showInputDialog("Insira o Nome:");
        if(nome == null){
            Thread.currentThread().stop();
        }
        partido.setNome(nome);
        String sigla = null;
        
            sigla = JOptionPane.showInputDialog("Insira a Sigla:");
        if(sigla == null){
            Thread.currentThread().stop();
        }
        partido.setSigla(sigla);
        partidoRepositorio.inserir(partido);
        carregarTabela();
    }//GEN-LAST:event_btnInserirMouseClicked

