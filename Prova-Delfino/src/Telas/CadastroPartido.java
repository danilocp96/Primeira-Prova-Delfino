    public void test(){
        PartidoRepositorio partidoRepositorio = new PartidoRepositorio();
        
        Integer row = tbPartidos.getSelectedRow();
        Integer col = tbPartidos.getSelectedColumn();
        String title = (String) tbPartidos.getColumnName(col);
        String nome = (String) tbPartidos.getValueAt(row, 0);
        Partido partido = partidoRepositorio.buscarPorNome(nome);
        
        switch (title) {
            case "Nome":
                String n = JOptionPane.showInputDialog("Digite o novo valor:");
                if(n == null){
                    break;
                }
                partido.setNome(n);
                break;
            case "Sigla":
                String sig = JOptionPane.showInputDialog("Digite o novo valor:");
                if(sig == null){
                    break;
                }
                partido.setSigla(sig);
                break;
            
        }
        
        partidoRepositorio.editar(partido);
        carregarTabela();
    }
    
    public void carregarTabela(){
        PartidoRepositorio partidoRepositorio = new PartidoRepositorio();
        List<Partido> partidos = partidoRepositorio.buscarTudoOrdenado();

        String[] colunasTabela = new String []{"Nome", "Sigla"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela){
            @Override
            public boolean isCellEditable(int row, int column) {
               //all cells false
               return false;
            }};
        tbPartidos.setModel(modeloTabela);
       
        for (Partido partido : partidos) {
            modeloTabela.addRow(new Object[]{partido.getNome(),partido.getSigla()});   
            }
       }
    
